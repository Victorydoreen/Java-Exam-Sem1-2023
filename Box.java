public class Box {
    private float width;

//    The constructor to initialize the width attribute of the box class
    public Box(float width) {
        this.width = width;
    }

    //     Getters and setters to help us in accessing the attribute and updating it
    public float getWidth() {
        return width;
    }

    public void setWidth(float width) {
        this.width = width;
    }
}
